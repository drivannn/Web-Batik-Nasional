          <div id="detail1" class="details">
            <h1>1. Periode Awal: Batik sebagai Tradisi Lokal</h1>

            <h3>Jejak Awal:</h3>
                <p>Tradisi menghias kain dengan teknik serupa batik diperkirakan telah ada di Nusantara sejak zaman prasejarah, namun bukti arkeologis langsung sulit ditemukan.
                  Tradisi ini berkembang dari teknik pewarnaan alami pada kain menggunakan bahan dari tumbuhan seperti indigo, mengkudu, dan soga.</p>
            <h3>Perkembangan di Jawa:</h3>
                <p>Pulau Jawa menjadi pusat utama perkembangan batik. Hal ini didukung oleh kehadiran kerajaan-kerajaan besar seperti Majapahit (1293â€“1527) dan kerajaan Hindu-Buddha lainnya yang mengintegrasikan seni ini ke dalam kehidupan masyarakat.
                  Batik awalnya digunakan dalam ritual keagamaan, adat istiadat, dan hanya untuk kalangan tertentu seperti keluarga kerajaan.
          </p>
        
        <h1>2. Masa Kerajaan Islam (Abad ke-16 - 18)</h1>
        
            <h3>Pada masa penyebaran Islam, batik mulai berkembang pesat di Jawa.
              Keraton sebagai Pusat Batik:</h3>
              
                <p>Batik menjadi simbol status sosial di kalangan bangsawan keraton, terutama di Yogyakarta dan Surakarta (Solo).
                  Motif batik keraton memiliki makna filosofis yang mendalam, seperti motif parang yang melambangkan kekuatan dan perjuangan, serta motif kawung yang merepresentasikan harmoni dan kesucian.
              Penyebaran ke Kalangan Rakyat:
                  Ketika anggota keraton berpindah ke luar istana, seni membatik pun menyebar ke masyarakat umum.</p>
        
        <h1>3. Masa Kolonial Belanda (Abad ke-19 - Awal Abad ke-20)</h1> 
        
            <h3>Perdagangan dan Eksposur Internasional:</h3>
              <p>Batik mulai dikenal di luar Indonesia melalui perdagangan yang dilakukan oleh pedagang Belanda.
              Belanda mengadopsi teknik membatik dan memopulerkannya di Eropa sebagai kain mewah.</p>
        
            <h3>Inovasi Teknik:</h3>
                <p>Teknik batik cap mulai diperkenalkan pada abad ke-19 sebagai upaya untuk memproduksi batik dalam jumlah besar secara lebih cepat.
                  Para pengusaha Tionghoa juga berkontribusi dengan menciptakan motif batik pesisir yang berwarna cerah, seperti batik Pekalongan.
          </p>
        <h1>4. Masa Pergerakan Nasional (Awal Abad ke-20)</h1>
            <p>Batik mulai digunakan sebagai simbol perlawanan terhadap kolonialisme.
              Kaum nasionalis Indonesia menggunakan batik sebagai wujud kebanggaan budaya lokal dan identitas nasional.</p>
        
        <h1>5. Masa Kemerdekaan dan Modernisasi (1945 - Sekarang)</h1>
        
            <h3>Simbol Identitas Bangsa:</h3>
                <p>Setelah kemerdekaan Indonesia pada tahun 1945, batik menjadi salah satu simbol kebanggaan nasional.
                  Pejabat negara sering memakai batik pada acara resmi untuk mempromosikan warisan budaya ini.</p>
        
            <h3>Pengakuan Dunia:</h3>
                <p>Pada 2 Oktober 2009, UNESCO menetapkan batik sebagai Masterpiece of Oral and Intangible Heritage of Humanity.
                  Batik semakin populer di dunia internasional sebagai kain tradisional yang unik.
          </p>
        
            <h3>Modernisasi:</h3>
                <p>Batik kini tidak hanya ditemukan pada kain tradisional tetapi juga pada berbagai produk modern, seperti pakaian kasual, aksesoris, hingga dekorasi rumah.
                  Generasi muda juga mulai melestarikan batik melalui inovasi desain yang lebih kreatif dan ramah pasar global.</p>
