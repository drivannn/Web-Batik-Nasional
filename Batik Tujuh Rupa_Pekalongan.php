              <h1>Ciri Khas Batik Tujuh Rupa</h1>
              
              
              <p>Batik Tujuh Rupa memiliki karakteristik sebagai berikut:</p>
              
              
              <ul>
              
                  <li>
              
                      <strong>Motif yang Beragam:</strong> Batik ini mencampurkan berbagai jenis motif, termasuk motif flora, fauna, geometris, dan juga simbol-simbol kebudayaan yang ada. Setiap motif bisa memiliki makna tersendiri dan menggambarkan pengaruh dari berbagai budaya yang pernah ada di Pekalongan.
              
                  </li>
              
                  <li>
              
                      <strong>Pengaruh Budaya yang Beragam:</strong> Pekalongan adalah tempat pertemuan berbagai kebudayaan, sehingga Batik Tujuh Rupa sering kali menunjukkan elemen-elemen khas dari Tionghoa, Arab, Eropa, dan juga budaya Jawa. Dalam satu kain batik, kita dapat melihat kombinasi motif yang berbeda, menggambarkan pertemuan berbagai budaya.
              
                  </li>
              
                  <li>
              
                      <strong>Keunikan Warna dan Desain:</strong> Batik Tujuh Rupa terkenal dengan penggunaan warna yang bervariasi dan cerah, yang tidak seperti batik tradisional lainnya yang lebih sederhana dan konservatif dalam warna. Warna-warna ini sering kali dipadukan dengan motif yang kaya dan rumit.
              
                  </li>
              
              </ul>
              
          
