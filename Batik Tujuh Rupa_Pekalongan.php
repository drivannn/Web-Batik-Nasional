            <h3>Batik Tujuh Rupa (Pekalongan)</h3>

            <p>
              Batik Tujuh Rupa muncul sekitar abad ke-19 hingga awal abad ke-20 di Pekalongan, yang merupakan kawasan pelabuhan strategis dan pusat perdagangan di Jawa. Kota ini banyak menerima pengaruh dari luar, baik dari China, Arab, Belanda, India, maupun Jepang, yang semuanya berperan dalam menciptakan kekayaan budaya, termasuk dalam seni batik.
            
            <p>
              Nama "Tujuh Rupa" merujuk pada konsep batik yang menggabungkan tujuh macam motif atau gaya dalam satu desain. Hal ini menggambarkan kekayaan variasi dan pengaruh yang ada dalam motif batik yang dihasilkan. Batik Tujuh Rupa pada umumnya memiliki ciri khas yang mencerminkan gabungan berbagai gaya, mulai dari motif tradisional Jawa, pengaruh Tionghoa, hingga desain yang terinspirasi oleh budaya Barat.
            
            </p>
            
